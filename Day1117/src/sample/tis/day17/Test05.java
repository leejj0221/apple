package sample.tis.day17;

public class Test05 {

	public static void main(String[] args) {
		// 83페이지 참고하세요
		// ++,-- 단항연산 증감연산 1씩증가 1씩감소
		int a = 7, b = 7, c = 7;
		a++;
		++b;
		c = c + 1;
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);

		int x = 7, y = 7, z = 7;
		x--;
		--y;
		z = z - 1;
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("z = " + z);
	}
}// class END
